export function getChaptersByBook(bookId) {
  return chapters[bookId] || []
}

export function getChapterById(bookId, chapterId) {
  const bookChapters = chapters[bookId]
  if (!bookChapters) return null
  return bookChapters.find(c => c.id === chapterId)
}

export function getChapterIndex(bookId, chapterId) {
  const bookChapters = chapters[bookId]
  if (!bookChapters) return -1
  return bookChapters.findIndex(c => c.id === chapterId)
}

export function getNextChapter(bookId, chapterId) {
  const bookChapters = chapters[bookId]
  if (!bookChapters) return null
  const index = bookChapters.findIndex(c => c.id === chapterId)
  if (index === -1 || index >= bookChapters.length - 1) return null
  return bookChapters[index + 1]
}

export function getPrevChapter(bookId, chapterId) {
  const bookChapters = chapters[bookId]
  if (!bookChapters) return null
  const index = bookChapters.findIndex(c => c.id === chapterId)
  if (index <= 0) return null
  return bookChapters[index - 1]
}

